-- Parseo de columnas por posición (TSV)
CREATE TEMP TABLE _stg_obras_sociales AS
SELECT
  NULLIF(BTRIM(cols[1]), '')::int AS osid,
  NULLIF(BTRIM(cols[2]), '') AS obra_social,
  COALESCE(NULLIF(BTRIM(cols[12]), ''), 'No') AS requiere_coseguro,
  COALESCE(NULLIF(BTRIM(cols[14]), ''), 'Activa') AS estado,
  COALESCE(
    CASE
      WHEN NULLIF(BTRIM(cols[15]), '') IS NOT NULL
      THEN TO_TIMESTAMP(BTRIM(cols[15]), 'YYYY-MM-DD HH24:MI:SS.MS')
      ELSE NULL
    END,
    NOW()
  ) AS fecha_estado
FROM (
  SELECT string_to_array(line, E'\t') AS cols
  FROM _raw_os
  WHERE BTRIM(line) <> ''
) t
WHERE NULLIF(BTRIM(cols[1]), '') ~ '^[0-9]+$'
  AND NULLIF(BTRIM(cols[2]), '') IS NOT NULL
  AND LOWER(COALESCE(NULLIF(BTRIM(cols[7]), ''), 'obra social')) = 'obra social';

DO $$
BEGIN
  IF to_regclass('public."ObraSocial"') IS NULL AND to_regclass('public.obrasocial') IS NULL THEN
    EXECUTE '
      CREATE TABLE "ObraSocial" (
        "OSID" integer PRIMARY KEY,
        "OSNom" varchar(255) NOT NULL,
        "OSReqCoseg" char(1) NOT NULL DEFAULT ''N'',
        "OSEstad" char(1) NOT NULL DEFAULT ''A'',
        "OSFchEst" timestamp(3) NOT NULL DEFAULT NOW()
      )
    ';
  END IF;

  IF to_regclass('public."ObraSocial"') IS NOT NULL THEN
    EXECUTE 'ALTER TABLE "ObraSocial" ADD COLUMN IF NOT EXISTS "OSNom" varchar(255)';
    EXECUTE 'ALTER TABLE "ObraSocial" ADD COLUMN IF NOT EXISTS "OSReqCoseg" char(1)';
    EXECUTE 'ALTER TABLE "ObraSocial" ADD COLUMN IF NOT EXISTS "OSEstad" char(1)';
    EXECUTE 'ALTER TABLE "ObraSocial" ADD COLUMN IF NOT EXISTS "OSFchEst" timestamp(3)';

    EXECUTE '
      INSERT INTO "ObraSocial" ("OSID", "OSNom", "OSReqCoseg", "OSEstad", "OSFchEst")
      SELECT
        s.osid,
        LEFT(s.obra_social, 255),
        CASE
          WHEN LOWER(REPLACE(COALESCE(s.requiere_coseguro, ''''), ''í'', ''i'')) IN (''si'', ''s'', ''1'', ''true'', ''t'') THEN ''S''
          ELSE ''N''
        END,
        CASE
          WHEN LOWER(REPLACE(COALESCE(s.estado, ''activa''), ''í'', ''i'')) IN (''activa'', ''activo'', ''a'', ''1'', ''true'', ''t'') THEN ''A''
          ELSE ''I''
        END,
        s.fecha_estado
      FROM _stg_obras_sociales s
      ON CONFLICT ("OSID") DO UPDATE SET
        "OSNom"      = EXCLUDED."OSNom",
        "OSReqCoseg" = EXCLUDED."OSReqCoseg",
        "OSEstad"    = EXCLUDED."OSEstad",
        "OSFchEst"   = EXCLUDED."OSFchEst"
    ';
  ELSE
    EXECUTE 'ALTER TABLE obrasocial ADD COLUMN IF NOT EXISTS osnom varchar(255)';
    EXECUTE 'ALTER TABLE obrasocial ADD COLUMN IF NOT EXISTS osreqcoseg char(1)';
    EXECUTE 'ALTER TABLE obrasocial ADD COLUMN IF NOT EXISTS osestad char(1)';
    EXECUTE 'ALTER TABLE obrasocial ADD COLUMN IF NOT EXISTS osfchest timestamp(3)';

    EXECUTE '
      INSERT INTO obrasocial (osid, osnom, osreqcoseg, osestad, osfchest)
      SELECT
        s.osid,
        LEFT(s.obra_social, 255),
        CASE
          WHEN LOWER(REPLACE(COALESCE(s.requiere_coseguro, ''''), ''í'', ''i'')) IN (''si'', ''s'', ''1'', ''true'', ''t'') THEN ''S''
          ELSE ''N''
        END,
        CASE
          WHEN LOWER(REPLACE(COALESCE(s.estado, ''activa''), ''í'', ''i'')) IN (''activa'', ''activo'', ''a'', ''1'', ''true'', ''t'') THEN ''A''
          ELSE ''I''
        END,
        s.fecha_estado
      FROM _stg_obras_sociales s
      ON CONFLICT (osid) DO UPDATE SET
        osnom      = EXCLUDED.osnom,
        osreqcoseg = EXCLUDED.osreqcoseg,
        osestad    = EXCLUDED.osestad,
